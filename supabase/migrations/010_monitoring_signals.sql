-- Migration 010: Monitoring signals + incidents + remediation log
--
-- Data model for the autonomous agent (Stage 1-4).
-- See docs/AUTONOMOUS_AGENT.md for architecture + rollout plan.
--
-- Idempotent. Safe to run multiple times.
-- Run in Supabase SQL Editor → paste + Run.
--
-- Tables added:
--   1. monitoring_signals — raw events from all data sources
--   2. incidents          — consolidated events needing human tracking
--   3. remediation_log    — every auto-action the agent took (Stage 3+)
--
-- RLS: service_role only. No anon/authenticated access.

-- ─────────────────────────────────────────────────────────────
-- 1. MONITORING SIGNALS (Stage 1 — always on)
-- ─────────────────────────────────────────────────────────────

create table if not exists public.monitoring_signals (
  id uuid default gen_random_uuid() primary key,
  created_at timestamptz not null default now(),

  -- Source of the signal (free-form string for flexibility)
  -- Examples: 'ops-check', 'sentry', 'stripe-webhook', 'supabase-query',
  --           'vercel-deploy', 'user-signup', 'ai-render', 'health-probe'
  source text not null,

  -- Severity — matches docs/AUTONOMOUS_AGENT.md classification matrix
  severity text not null default 'not-assessed'
    check (severity in ('not-assessed', 'normal', 'info', 'watch', 'warn', 'critical')),

  -- Human-readable short message (max 500 chars)
  message text not null check (length(message) <= 500),

  -- Structured payload (metrics, stack traces, query details, etc.)
  details jsonb not null default '{}'::jsonb,

  -- Deduplication fingerprint — same-type events collapse
  -- Generated by collector as e.g. sha256(source || '|' || message_template)
  fingerprint text,

  -- Link to consolidated incident (null = standalone signal)
  incident_id uuid references public.incidents(id) on delete set null,

  -- Agent processing state
  classified_at timestamptz,
  notified_at timestamptz,
  notified_channel text -- 'telegram' | 'email' | 'sentry' | null
);

-- Indexes for common query patterns
create index if not exists idx_monitoring_signals_created_at
  on public.monitoring_signals(created_at desc);
create index if not exists idx_monitoring_signals_severity
  on public.monitoring_signals(severity, created_at desc)
  where severity in ('warn', 'critical');
create index if not exists idx_monitoring_signals_source
  on public.monitoring_signals(source, created_at desc);
create index if not exists idx_monitoring_signals_fingerprint
  on public.monitoring_signals(fingerprint)
  where fingerprint is not null;
create index if not exists idx_monitoring_signals_incident
  on public.monitoring_signals(incident_id)
  where incident_id is not null;

-- ─────────────────────────────────────────────────────────────
-- 2. INCIDENTS (Stage 2+ — consolidated events)
-- ─────────────────────────────────────────────────────────────

create table if not exists public.incidents (
  id uuid default gen_random_uuid() primary key,
  opened_at timestamptz not null default now(),
  closed_at timestamptz,

  severity text not null default 'watch'
    check (severity in ('info', 'watch', 'warn', 'critical')),

  title text not null check (length(title) <= 200),
  description text,

  status text not null default 'open'
    check (status in ('open', 'acknowledged', 'remediated', 'closed', 'post-mortem-pending', 'closed-no-action')),

  resolution text,

  -- Who acknowledged/closed (email or 'agent' for automated)
  human_ack_by text,
  human_ack_at timestamptz,

  -- Link to post-mortem document if required
  post_mortem_path text,

  -- How long before human acknowledgment (computed at ack time)
  time_to_ack_seconds integer,

  -- How long from open to close
  time_to_close_seconds integer
);

create index if not exists idx_incidents_status
  on public.incidents(status, opened_at desc)
  where status != 'closed';
create index if not exists idx_incidents_severity
  on public.incidents(severity, opened_at desc);
create index if not exists idx_incidents_opened_at
  on public.incidents(opened_at desc);

-- Auto-update time_to_close when status changes to closed
create or replace function public.incidents_update_times()
returns trigger language plpgsql as $$
begin
  if new.status = 'acknowledged' and old.status = 'open' and new.human_ack_at is null then
    new.human_ack_at := now();
    new.time_to_ack_seconds := extract(epoch from (now() - new.opened_at))::integer;
  end if;
  if new.status in ('closed', 'closed-no-action') and new.closed_at is null then
    new.closed_at := now();
    new.time_to_close_seconds := extract(epoch from (now() - new.opened_at))::integer;
  end if;
  return new;
end;
$$;

drop trigger if exists trg_incidents_update_times on public.incidents;
create trigger trg_incidents_update_times
  before update on public.incidents
  for each row execute function public.incidents_update_times();

-- ─────────────────────────────────────────────────────────────
-- 3. REMEDIATION LOG (Stage 3+ — auto-actions audit trail)
-- ─────────────────────────────────────────────────────────────

create table if not exists public.remediation_log (
  id uuid default gen_random_uuid() primary key,
  created_at timestamptz not null default now(),
  incident_id uuid references public.incidents(id) on delete set null,

  -- Playbook that triggered this action
  playbook_name text not null,

  -- Action classification
  action_type text not null
    check (action_type in ('config_change', 'fallback', 'restart', 'cache_purge',
                           'rate_limit', 'engine_disable', 'notify_only',
                           'escalate', 'circuit_break')),

  -- Full action payload
  action_details jsonb not null default '{}'::jsonb,

  -- Did the action succeed technically?
  success boolean not null default true,

  -- Did a human have to undo this action? (critical signal — bad playbook)
  human_reversed boolean not null default false,
  human_reversed_at timestamptz,
  human_reversed_by text,
  human_reversed_reason text,

  -- Notes (LLM reasoning, context, etc.)
  notes text
);

create index if not exists idx_remediation_log_incident
  on public.remediation_log(incident_id, created_at desc);
create index if not exists idx_remediation_log_playbook
  on public.remediation_log(playbook_name, created_at desc);
create index if not exists idx_remediation_log_reversed
  on public.remediation_log(human_reversed, created_at desc)
  where human_reversed = true;

