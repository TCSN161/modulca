-- Insert signal with dedup — called by monitor-collect.mjs via RPC
create or replace function public.agent_insert_signal(
  p_source text,
  p_severity text,
  p_message text,
  p_details jsonb default '{}'::jsonb,
  p_fingerprint text default null,
  p_dedup_window_minutes integer default 5
)
returns uuid
language plpgsql security definer set search_path = public
as $$
declare
  v_existing_id uuid;
  v_new_id uuid;
begin
  -- Dedup: if same fingerprint seen in last N minutes, skip insertion
  if p_fingerprint is not null then
    select id into v_existing_id
    from monitoring_signals
    where fingerprint = p_fingerprint
      and created_at > now() - (p_dedup_window_minutes || ' minutes')::interval
    order by created_at desc
    limit 1;

    if v_existing_id is not null then
      return v_existing_id;  -- dedup hit
    end if;
  end if;

  -- Insert fresh signal
  insert into monitoring_signals (source, severity, message, details, fingerprint)
  values (p_source, p_severity, p_message, p_details, p_fingerprint)
  returning id into v_new_id;

  return v_new_id;
end;
$$;

-- Get active incidents (for dashboard + Telegram /incidents command)
create or replace function public.agent_active_incidents(limit_n integer default 20)
returns table (
  id uuid,
  opened_at timestamptz,
  severity text,
  title text,
  status text,
  age_minutes integer,
  signal_count bigint
)
language plpgsql security definer set search_path = public
as $$
begin
  return query
  select
    i.id,
    i.opened_at,
    i.severity,
    i.title,
    i.status,
    extract(epoch from (now() - i.opened_at))::integer / 60 as age_minutes,
    count(s.id) as signal_count
  from incidents i
  left join monitoring_signals s on s.incident_id = i.id
  where i.status not in ('closed', 'closed-no-action')
  group by i.id
  order by
    case i.severity
      when 'critical' then 1
      when 'warn' then 2
      when 'watch' then 3
      else 4
    end,
    i.opened_at desc
  limit limit_n;
end;
$$;

-- Get recent signals by severity (for dashboard tile)
create or replace function public.agent_recent_signals(
  p_min_severity text default 'watch',
  p_hours integer default 24,
  p_limit integer default 50
)
returns table (
  id uuid,
  created_at timestamptz,
  source text,
  severity text,
  message text,
  incident_id uuid
)
language plpgsql security definer set search_path = public
as $$
declare
  v_severity_levels text[] := array['normal', 'info', 'watch', 'warn', 'critical'];
  v_min_index integer;
begin
  v_min_index := array_position(v_severity_levels, p_min_severity);

  return query
  select s.id, s.created_at, s.source, s.severity, s.message, s.incident_id
  from monitoring_signals s
  where s.created_at > now() - (p_hours || ' hours')::interval
    and array_position(v_severity_levels, s.severity) >= v_min_index
  order by s.created_at desc
  limit p_limit;
end;
$$;

-- Stats snapshot (for Telegram /status + dashboard overview)
create or replace function public.agent_health_snapshot()
returns jsonb
language plpgsql security definer set search_path = public
as $$
declare
  result jsonb;
begin
  select jsonb_build_object(
    'snapshot_at', now(),
    'last_24h_signals', (
      select count(*) from monitoring_signals
      where created_at > now() - interval '24 hours'
    ),
    'last_24h_by_severity', (
      select jsonb_object_agg(severity, cnt)
      from (
        select severity, count(*) as cnt
        from monitoring_signals
        where created_at > now() - interval '24 hours'
        group by severity
      ) t
    ),
    'open_incidents', (
      select count(*) from incidents
      where status not in ('closed', 'closed-no-action')
    ),
    'critical_open', (
      select count(*) from incidents
      where status not in ('closed', 'closed-no-action')
        and severity = 'critical'
    ),
    'last_critical', (
      select jsonb_build_object('title', title, 'opened_at', opened_at)
      from incidents
      where severity = 'critical'
      order by opened_at desc
      limit 1
    ),
    'recent_remediations_24h', (
      select count(*) from remediation_log
      where created_at > now() - interval '24 hours'
    ),
    'reversed_remediations_24h', (
      select count(*) from remediation_log
      where created_at > now() - interval '24 hours' and human_reversed = true
    )
  ) into result;

  return result;
end;
$$;

